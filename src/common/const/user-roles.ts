/**
 * Enum containing user roles.
 *
 * @export
 * @enum {string}
 */
export enum UserRoles {
    /** Administrator role. */
    ADMIN = 'ADMIN',
    /** Authorized user role. */
    AUTHORIZED = 'AUTHORIZED',
    /** Current authorized user. */
    CURRENT = 'CURRENT',
    /** Guest user role. */
    GUEST = 'GUEST',
    /** Moderator role. */
    MODERATOR = 'MODERATOR',
}
