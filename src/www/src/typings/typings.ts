/**
 * Component default props interface.
 *
 * @export
 * @interface IDefaultProps
 */
export interface IDefaultProps {
    /**
     * Component id.
     *
     * @memberof IDefaultProps
     */
    id?: string;
    /**
     * Component classname.
     *
     * @memberof IDefaultProps
     */
    className?: string;
    /**
     * Component key.
     *
     * @memberof IDefaultProps
     */
    key?: string;
    /**
     * Component styles.
     *
     * @type {React.CSSProperties}
     * @memberof IDefaultProps
     */
    style?: React.CSSProperties;
}
